		,	layerData						(i_layerData)	/**< レイヤーデータ */
		,	inputBufferCount				(0)				/**< 入力バッファ数 */
		,	outputBufferCount				(0)				/**< 出力バッファ数 */
		,	m_lppInputBuffer				(NULL)			/**< 演算時の入力データ */
	{
		cublasCreate(&cublasHandle);
	}
	/** デストラクタ */
	ChooseChannel_GPU::~ChooseChannel_GPU()
	{
		cublasDestroy(cublasHandle);
	}


